int init_amp_coordtran(amp_coordtran *acs_array,int n_acs) {
  int i;
  amp_coordtran *acs;
  for (i=0;i<n_acs;i++) {
    acs=&acs_array[i];
    sscanf(amp_coordtran_str[i],
           "R%1d%1dS%1d%1dC%1d%1d "
           "%f %f %f %f %f %f "
           "%f %f %f %f %f %f "
           "%d %d %d %d "
           "%f %f %f %f",
           &acs->raft_ix[0],       &acs->raft_ix[1],
           &acs->sens_ix[0],       &acs->sens_ix[1],
           &acs->device_ix[0],     &acs->device_ix[1],
           &acs->xo[0],            &acs->xo[1],
           &acs->xc[0][0],         &acs->xc[0][1],
           &acs->xc[1][0],         &acs->xc[1][1],
           &acs->po[0],            &acs->po[1],
           &acs->pc[0][0],         &acs->pc[0][1],
           &acs->pc[1][0],         &acs->pc[1][1],
           &acs->addr_lim[0][0],   &acs->addr_lim[0][1],           
           &acs->addr_lim[1][0],   &acs->addr_lim[1][1],           
           &acs->addr_elim[0][0],  &acs->addr_elim[0][1],          
           &acs->addr_elim[1][0],  &acs->addr_elim[1][1]
           );
  }
  return(0);
}

void amp2sens(amp_coordtran *ampDesc, int iAmpPix, int jAmpPix, int *iSensPix, int *jSensPix) {

#define FID_device_i0 1
#define FID_device_i1 7

  int sens_i0 = ampDesc->sens_ix[0];
  int sens_i1 = ampDesc->sens_ix[1];
  int raft_i0 = ampDesc->raft_ix[0];
  int raft_i1 = ampDesc->raft_ix[1];
  int device_i0 = ampDesc->device_ix[0];
  int device_i1 = ampDesc->device_ix[1];

  amp_coordtran *acs = NULL;
  amp_coordtran *acsFid = NULL;

  int i = n_amp_coordtran;
  while (i--) {
    amp_coordtran *acsCheck=&amp_coord_tran[i];
    if (acsCheck->sens_ix[0]==sens_i0 && acsCheck->sens_ix[1]==sens_i1
	&& acsCheck->raft_ix[0]==raft_i0 && acsCheck->raft_ix[1]==raft_i1) {
      /* we have the right raft and sensor */
      
      if (acsCheck->device_ix[0] == device_i0 && acsCheck->device_ix[1] == device_i1) {
	acs = acsCheck;
      }

      if (acsCheck->device_ix[0] == FID_device_i0 && acsCheck->device_ix[1] == FID_device_i1) {
	acsFid = acsCheck;
      }
    }
  }

  if (!acs || !acsFid) {
    printf("oops\n");
    return;
  }

  /* transform the input pixel into the fiducial pixel frame */

  /* first get the ccs coords of the input pixel */

  float x0 = acs->xo[0] + acs->xc[0][0]*iAmpPix + acs->xc[0][1]*jAmpPix;
  float x1 = acs->xo[1] + acs->xc[1][0]*iAmpPix + acs->xc[1][1]*jAmpPix;

  /* now transform those ccs coords to the fiducial pixel frame */

  *iSensPix = acsFid->po[0] + acsFid->pc[0][0]*x0   + acsFid->pc[0][1]*x1;
  *jSensPix = acsFid->po[1] + acsFid->pc[1][0]*x0   + acsFid->pc[1][1]*x1;

}

int ccs2amp(double xFp, double yFp, amp_coordtran *ampDesc, int *iPix, int *jPix) {

  if (!initialized) {
    init_amp_coordtran(amp_coord_tran,n_amp_coordtran);
    initialized = 1;
    printf("Initialized\n");
  }

  printf("x,y: %lf %lf\n", xFp, yFp);

  int i;
  float p[2],x[2];
  int ip[2];
  char s[2048];
  amp_coordtran *acs;

  i=n_amp_coordtran;
  while (i--) {
    acs=&amp_coord_tran[i];
    
    p[0]=acs->po[0]+acs->pc[0][0]*xFp+acs->pc[0][1]*yFp;
    p[1]=acs->po[1]+acs->pc[1][0]*xFp+acs->pc[1][1]*yFp;
    if (p[0] < acs->addr_elim[0][0]) continue;
    if (p[0] > acs->addr_elim[0][1]) continue;
    if (p[1] < acs->addr_elim[1][0]) continue;
    if (p[1] > acs->addr_elim[1][1]) continue;
    
    ip[0] = floor(p[0]);
    ip[1] = floor(p[1]);

    // nonuniform pixel sizes: finite lateral E-field @ guard ring,
    // pixel grid nonuniformity etc.
    // on the addr_i axis
    if (ip[0] > acs->addr_lim[0][1])   ip[0]=acs->addr_lim[0][1];
    if (ip[0] < acs->addr_lim[0][0])   ip[0]=acs->addr_lim[0][0];
    // on the addr_j axis
    if (ip[1] > acs->addr_lim[1][1])   ip[1]=acs->addr_lim[1][1];
    if (ip[1] < acs->addr_lim[1][0])   ip[1]=acs->addr_lim[1][0];

    printf("{x,y} => {%+9.4lf,%+9.4lf}\n"
             "amplifier => R%1d%1dS%1d%1dC%1d%1d\n"
	     "{p0,p1} => {%7.3f,%7.3f}\n"
             "{addr0,addr1} => {%4d,%4d}\n",
             xFp,yFp,
             acs->raft_ix[0],        acs->raft_ix[1],
             acs->sens_ix[0],        acs->sens_ix[1],
             acs->device_ix[0],      acs->device_ix[1],
             p[0],p[1],
             ip[0],ip[1]
             );
    *ampDesc = *acs;
    *iPix = ip[0];
    *jPix = ip[1];
    return 0;
    }

    // if control arrives here the point is not registered on any amplifier.
   
  printf("Coords not on a detector");
    return -1;
    
}

